using UnityEngine;
using System.Collections;

namespace STB.ADAOPS
{
	///////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>
	/// Class: CraterCreator
	/// # To create a bloody crater creator
	/// </summary>
	///////////////////////////////////////////////////////////////////////////////////////////////////////
	public class CraterCreator : DirtyObject
	{
		///////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>
		/// Start
		/// # Start the class and set some values
		/// </summary>
		///////////////////////////////////////////////////////////////////////////////////////////////////////
		void Start ()
		{
			continuousMode = false;
			destroyTime = 4;
			scaleMultiplierRange = new Vector2 (0.5f, 1);
			rotationRange = new Vector2 (0, 360);
			timeBetweenDecalCreations = 9999;
		}
		///////////////////////////////////////////////////////////////////////////////////////////////////////
		/// <summary>
		/// OnCollisionEnterExtended -- OVERRIDED
		/// Extends OnCollisionEnter
		/// </summary>
		///////////////////////////////////////////////////////////////////////////////////////////////////////
		protected override void OnCollisionEnterExtended (Collision col)
		{
			Destroy(this.gameObject, 0);
		}
	}
}